  echo ""
  echo "–ó–∞–±–µ–ª–µ–∂–∫–∞: –°–∫—Ä–∏–ø—Ç—ä—Ç –∏–∑–∏—Å–∫–≤–∞ –ø—Ä–µ–¥–∏ —Ç–æ–≤–∞ –¥–∞ –µ –∏–∑–ø—ä–ª–Ω–µ–Ω —É—Å–ø–µ—à–Ω–æ:"
