    exit 1
  fi
  echo "‚úÖ Bind9 –µ —Ä–µ—Å—Ç–∞—Ä—Ç–∏—Ä–∞–Ω —É—Å–ø–µ—à–Ω–æ."
  echo ""

  # -------------------------------------------------------------------------------------
  # –°–ï–ö–¶–ò–Ø 6: –ó–∞–ø–∏—Å –Ω–∞ —Ä–µ–∑—É–ª—Ç–∞—Ç
  # -------------------------------------------------------------------------------------
