#    1. –ü—Ä–µ–¥–≤–∞—Ä–∏—Ç–µ–ª–Ω–∏ –ø—Ä–æ–≤–µ—Ä–∫–∏ (OS, IP, FQDN, –ø—Ä–µ–¥–∏—à–Ω–æ –∏–∑–ø—ä–ª–Ω–µ–Ω–∏–µ)
#    2. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∏ –ø–æ–¥—Å–∏–≥—É—Ä—è–≤–∞–Ω–µ –Ω–∞ –±–∞–∑–æ–≤–∏ –ø–æ–ª–∏—Ç–∏–∫–∏
