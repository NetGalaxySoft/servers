    exit 1
  fi
  echo "‚úÖ –°–∏–Ω—Ç–∞–∫—Å–∏—Å—ä—Ç –µ –≤–∞–ª–∏–¥–µ–Ω."
  echo ""

  # -------------------------------------------------------------------------------------
  # –°–ï–ö–¶–ò–Ø 5: –†–µ—Å—Ç–∞—Ä—Ç –Ω–∞ Bind9
