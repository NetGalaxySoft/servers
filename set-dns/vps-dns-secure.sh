  else
    echo "SECURE_DNS_MODULE3=‚úÖ" | sudo tee -a "$SETUP_ENV_FILE" > /dev/null
  fi

  echo "‚úÖ –ú–æ–¥—É–ª 3 –∑–∞–≤—ä—Ä—à–∏ —É—Å–ø–µ—à–Ω–æ."
  echo ""
fi
echo ""
echo ""






exit 0


