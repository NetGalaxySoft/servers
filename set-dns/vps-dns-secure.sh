#  (Bind9), —Å–ª–µ–¥ –∫–∞—Ç–æ –µ –∏–∑–ø—ä–ª–Ω–µ–Ω —Å–∫—Ä–∏–ø—Ç—ä—Ç vps-dns-qsetup.sh.
