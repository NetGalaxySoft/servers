    }' "$OPTIONS_FILE"
    echo "‚úÖ also-notify –¥–æ–±–∞–≤–µ–Ω –∑–∞ PRIMARY."
  fi

  echo ""

  # -------------------------------------------------------------------------------------
  # –°–ï–ö–¶–ò–Ø 4: –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Å–∏–Ω—Ç–∞–∫—Å–∏—Å–∞
