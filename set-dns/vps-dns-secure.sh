  echo ""
  echo "–ó–∞–±–µ–ª–µ–∂–∫–∞: –°–∫—Ä–∏–ø—Ç—ä—Ç –∏–∑–∏—Å–∫–≤–∞ –¥–∞ –µ –∏–∑–ø—ä–ª–Ω–µ–Ω vps-dns-qsetup.sh –ø—Ä–µ–¥–∏ –Ω–µ–≥–æ."
