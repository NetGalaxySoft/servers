#!/bin/bash

# ==========================================================================
#  vps-dns-qsetup - –ê–≤—Ç–æ–º–∞—Ç–∏–∑–∏—Ä–∞–Ω–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –Ω–∞ DNS —Å—ä—Ä–≤—ä—Ä (Bind9)
# --------------------------------------------------------------------------
