#    5. –ê–∫—Ç–∏–≤–∏—Ä–∞–Ω–µ –∏ —Ç–µ—Å—Ç–≤–∞–Ω–µ –Ω–∞ DNS —É—Å–ª—É–≥–∞—Ç–∞
# ==========================================================================

