# === [4] –ò–ó–ë–û–† –ù–ê PHP –í–ï–†–°–ò–Ø ===============================================

echo ""
echo "üßÆ –û—Ç–∫—Ä–∏–≤–∞–Ω–µ –Ω–∞ –Ω–∞–ª–∏—á–Ω–∏—Ç–µ PHP –≤–µ—Ä—Å–∏–∏..."

# –°–ø–∏—Å—ä–∫ —Å –≤—Å–∏—á–∫–∏ –ø–æ–¥–¥—ä—Ä–∂–∞–Ω–∏ –≤–µ—Ä—Å–∏–∏ –æ—Ç ondrej/php
ALL_PHP_VERSIONS=(8.3 8.2 8.1 8.0 7.4 7.3 7.2 7.1 7.0 5.6)
