  echo "–ü–æ–∑–≤–æ–ª—è–≤–∞ –∏–∑–±–æ—Ä –Ω–∞ PHP –≤–µ—Ä—Å–∏—è –∏ —Ç–∏–ø —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç (Let's Encrypt –∏–ª–∏ —Å–æ–±—Å—Ç–≤–µ–Ω)."
