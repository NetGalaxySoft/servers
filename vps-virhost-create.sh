# === [3] –ù–ê–ß–ê–õ–ù–ê –°–¢–†–ê–ù–ò–¶–ê ==========================================

echo ""
echo "–ù–∞—á–∞–ª–Ω–∞—Ç–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–∞ –Ω–∞ –≤–∞—à–∏—è —Ö–æ—Å—Ç —â–µ –ø–æ–∫–∞–∑–≤–∞ —Å–ª–µ–¥–Ω–æ—Ç–æ —Å—ä–æ–±—â–µ–Ω–∏–µ:"
