  echo "‚ÑπÔ∏è –ù—è–º–∞ –≤—ä–≤–µ–¥–µ–Ω–æ —Å—ä–æ–±—â–µ–Ω–∏–µ ‚Äì index.html —â–µ —Å—ä–¥—ä—Ä–∂–∞ —Å–∞–º–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–µ–Ω —Ç–µ–∫—Å—Ç."
