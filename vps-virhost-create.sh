read -rp "üí¨ –í—ä–≤–µ–¥–µ—Ç–µ —Å—ä–æ–±—â–µ–Ω–∏–µ (–¥–æ 160 —Å–∏–º–≤–æ–ª–∞). –ù–∞—Ç–∏—Å–Ω–µ—Ç–µ Enter –∑–∞ –ø—Ä–æ–ø—É—Å–∫–∞–Ω–µ –∏–ª–∏ 'q' –∑–∞ –ø—Ä–µ–∫—Ä–∞—Ç—è–≤–∞–Ω–µ: " custom_msg

