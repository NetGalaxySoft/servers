menu_index=1

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ—è –µ –∏–Ω—Å—Ç–∞–ª–∏—Ä–∞–Ω–∞ –∏ –∫–æ—è –Ω–µ
