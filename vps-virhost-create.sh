  echo "üö™ –ü—Ä–µ–∫—Ä–∞—Ç—è–≤–∞–Ω–µ –ø–æ –∑–∞—è–≤–∫–∞ –Ω–∞ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞."
  exit 0
