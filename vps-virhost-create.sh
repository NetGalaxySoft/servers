# –û—Ç–∫—Ä–∏–≤–∞–Ω–µ –Ω–∞ —Ä–µ–∞–ª–Ω–∏—è –ø—É–±–ª–∏—á–µ–Ω IP –∞–¥—Ä–µ—Å –Ω–∞ —Å—ä—Ä–≤—ä—Ä–∞
ACTUAL_IP=$(curl -s https://api.ipify.org)

if [[ -z "$ACTUAL_IP" ]]; then
  echo "‚ùå –ù–µ—É—Å–ø–µ—à–Ω–æ –æ—Ç–∫—Ä–∏–≤–∞–Ω–µ –Ω–∞ –ø—É–±–ª–∏—á–Ω–∏—è IP –∞–¥—Ä–µ—Å. –ü—Ä–æ–≤–µ—Ä–µ—Ç–µ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç –≤—Ä—ä–∑–∫–∞—Ç–∞."
  exit 1
