# –ü–æ–¥–≥–æ—Ç–æ–≤–∫–∞ –Ω–∞ –ø—Ä–æ–º–µ–Ω–ª–∏–≤–∏ –∑–∞ —Å—ä–±–∏—Ä–∞–Ω–µ –Ω–∞ –¥–∞–Ω–Ω–∏
SUMMARY_DOMAIN=""
SUMMARY_ROOT_DOMAIN=""
SUMMARY_IS_SUBDOMAIN=""
SUMMARY_WEBROOT=""
SUMMARY_PHP_VERSION=""
SUMMARY_SSL_TYPE=""
SUMMARY_CUSTOM_MESSAGE=""

echo "=================================================================="
echo " üåê NetGalaxy - –°—ä–∑–¥–∞–≤–∞–Ω–µ –Ω–∞ –≤–∏—Ä—Ç—É–∞–ª–µ–Ω —Ö–æ—Å—Ç –∑–∞ Apache —Å—ä—Ä–≤—ä—Ä"
