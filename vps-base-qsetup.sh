  fi

  if id "$ADMIN_USER" &>/dev/null; then
    echo "‚ö†Ô∏è –ü–æ—Ç—Ä–µ–±–∏—Ç–µ–ª—è—Ç '$ADMIN_USER' –≤–µ—á–µ —Å—ä—â–µ—Å—Ç–≤—É–≤–∞. –ò–∑–±–µ—Ä–µ—Ç–µ –¥—Ä—É–≥–æ –∏–º–µ."
    continue
  fi

  break
done

while true; do
  printf "üîë –í—ä–≤–µ–¥–µ—Ç–µ –ø–∞—Ä–æ–ª–∞ –∑–∞ $ADMIN_USER: "
  read -s PASSWORD_1
