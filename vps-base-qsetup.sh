    continue
  fi

  if [[ ! "$ADMIN_USER" =~ ^[a-z_][a-z0-9_-]*$ ]]; then
    echo "‚ùå –ù–µ–≤–∞–ª–∏–¥–Ω–æ –ø–æ—Ç—Ä–µ–±–∏—Ç–µ–ª—Å–∫–æ –∏–º–µ. –†–∞–∑—Ä–µ—à–µ–Ω–∏ —Å–∞ —Å–∞–º–æ –º–∞–ª–∫–∏ –±—É–∫–≤–∏, —Ü–∏—Ñ—Ä–∏, '-', '_' –∏ –¥–∞ –Ω–µ –∑–∞–ø–æ—á–≤–∞ —Å —Ü–∏—Ñ—Ä–∞."
