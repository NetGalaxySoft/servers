echo -e " –ù–ê–ß–ê–õ–ù–ê –ö–û–ù–§–ò–ì–£–†–ê–¶–ò–Ø –ù–ê –û–¢–î–ê–õ–ï–ß–ï–ù –°–™–†–í–™–†"
