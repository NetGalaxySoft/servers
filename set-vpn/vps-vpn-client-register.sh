  echo -e "üì± \e[1;36mQR –∫–æ–¥:\e[0m               $CLIENTS_DIR/$CLIENT_NAME.png"
else
  echo -e "üì± \e[1;36mQR –∫–æ–¥:\e[0m                (–ù–µ –µ –≥–µ–Ω–µ—Ä–∏—Ä–∞–Ω)"
