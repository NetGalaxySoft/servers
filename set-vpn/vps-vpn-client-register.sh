# ‚úÖ –§—É–Ω–∫—Ü–∏—è –∑–∞ –∞–Ω–∏–º–∞—Ü–∏—è —Å —Ç–æ—á–∫–∏ (—É–Ω–∏–≤–µ—Ä—Å–∞–ª–Ω–∞)
animate_dots() {
