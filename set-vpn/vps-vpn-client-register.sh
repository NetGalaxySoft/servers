# ‚úÖ –í—ä–≤–µ–∂–¥–∞–Ω–µ –Ω–∞ IP –æ—Ç –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ (–∏–ª–∏ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–æ –ø–æ –ø–æ–¥—Ä–∞–∑–±–∏—Ä–∞–Ω–µ)
while true; do
  read -p "üåê –í—ä–≤–µ–¥–µ—Ç–µ VPN IP –∑–∞ –∫–ª–∏–µ–Ω—Ç–∞ [–ø–æ –ø–æ–¥—Ä–∞–∑–±–∏—Ä–∞–Ω–µ $FREE_IP]: " CLIENT_IP
  CLIENT_IP=${CLIENT_IP:-$FREE_IP}

  # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞ –≤–∞–ª–∏–¥–µ–Ω IPv4
  if [[ ! "$CLIENT_IP" =~ ^[0-9]+\.[0-9]+\.[0-9]+\.[0-9]+$ ]]; then
    echo "‚ùå –ù–µ–≤–∞–ª–∏–¥–µ–Ω IP –∞–¥—Ä–µ—Å."
    continue
  fi

  # –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ –Ω–µ –µ IP –Ω–∞ —Å—ä—Ä–≤—ä—Ä–∞
  if [[ "$CLIENT_IP" == "$SERVER_IP" || "$CLIENT_IP" == "$SUBNET_IP" ]]; then
    echo "‚ùå –¢–æ–∑–∏ IP –µ —Ä–µ–∑–µ—Ä–≤–∏—Ä–∞–Ω (—Å—ä—Ä–≤—ä—Ä –∏–ª–∏ –º—Ä–µ–∂–æ–≤ –∞–¥—Ä–µ—Å)."
    continue
  fi

  # –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ IP –µ –≤ –ø–æ–¥–º—Ä–µ–∂–∞—Ç–∞
  if ! ip_in_subnet "$CLIENT_IP" "$SUBNET_IP" "$SUBNET_MASK"; then
    echo "‚ùå IP –∞–¥—Ä–µ—Å—ä—Ç –Ω–µ –µ –≤ –ø–æ–¥–º—Ä–µ–∂–∞—Ç–∞ $VPN_SUBNET."
    continue
  fi

  # –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ IP –≤–µ—á–µ —Å–µ –∏–∑–ø–æ–ª–∑–≤–∞
  if sudo grep -q "$CLIENT_IP/32" "$WG_CONF"; then
    echo "‚ùå IP –∞–¥—Ä–µ—Å—ä—Ç –≤–µ—á–µ —Å–µ –∏–∑–ø–æ–ª–∑–≤–∞."
    continue
  fi

  break
done

# ‚úÖ –£—Å—Ç–∞–Ω–æ–≤—è–≤–∞–Ω–µ –Ω–∞ AllowedIPs –ø–æ –ø–æ–¥—Ä–∞–∑–±–∏—Ä–∞–Ω–µ
ALLOWED_IPS="0.0.0.0/0"

echo ""
echo "‚úÖ IP –∞–¥—Ä–µ—Å—ä—Ç –µ –≤–∞–ª–∏–¥–∏—Ä–∞–Ω: $CLIENT_IP"
echo "‚úÖ Allowed IPs: $ALLOWED_IPS"
echo ""
echo ""


# =====================================================================
# [–ú–û–î–£–õ 4] –ì–ï–ù–ï–†–ò–†–ê–ù–ï –ù–ê –ö–õ–Æ–ß–û–í–ï –ò –ö–û–ù–§–ò–ì–£–†–ê–¶–ò–Ø –ó–ê –ö–õ–ò–ï–ù–¢–ê
# =====================================================================

CLIENTS_DIR="/etc/wireguard/clients"
CLIENT_CONF="$CLIENTS_DIR/$CLIENT_NAME.conf"

# ‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–∞–ª–∏ –∫–ª–∏–µ–Ω—Ç—ä—Ç –≤–µ—á–µ —Å—ä—â–µ—Å—Ç–≤—É–≤–∞
if [[ -f "$CLIENT_CONF" ]]; then
  echo "‚ö†Ô∏è –ö–ª–∏–µ–Ω—Ç —Å –∏–º–µ '$CLIENT_NAME' –≤–µ—á–µ —Å—ä—â–µ—Å—Ç–≤—É–≤–∞."
  echo "‚ÑπÔ∏è –°—Ç–∞—Ä–∏—è—Ç –∑–∞–ø–∏—Å —â–µ –±—ä–¥–µ –ø—Ä–µ–º–∞—Ö–Ω–∞—Ç –∏ —Å—ä–∑–¥–∞–¥–µ–Ω –Ω–∞–Ω–æ–≤–æ."
  sudo rm -f "$CLIENT_CONF"
fi

# ‚úÖ –ü—Ä–µ–º–∞—Ö–≤–∞–Ω–µ –Ω–∞ —Å—Ç–∞—Ä–∏—è –±–ª–æ–∫ –æ—Ç wg0.conf (–∞–∫–æ —Å—ä—â–µ—Å—Ç–≤—É–≤–∞)
if sudo grep -q "# $CLIENT_NAME START" "$WG_CONF"; then
  echo "‚ÑπÔ∏è –ü—Ä–µ–º–∞—Ö–≤–∞–Ω–µ –Ω–∞ —Å—Ç–∞—Ä–∏—è –∑–∞–ø–∏—Å –æ—Ç wg0.conf..."
  sudo sed -i "/# $CLIENT_NAME START/,/# $CLIENT_NAME END/d" "$WG_CONF"
fi

# ‚úÖ –ì–µ–Ω–µ—Ä–∏—Ä–∞–Ω–µ –Ω–∞ –∫–ª—é—á–æ–≤–µ –∑–∞ –∫–ª–∏–µ–Ω—Ç–∞ (—Å –∞–Ω–∏–º–∞—Ü–∏—è)
echo -n "üîë –ì–µ–Ω–µ—Ä–∏—Ä–∞–Ω–µ –Ω–∞ –∫–ª—é—á–æ–≤–µ"
animate_dots 0.2 &
ANIM_PID=$!

CLIENT_PRIVATE_KEY=$(wg genkey)
CLIENT_PUBLIC_KEY=$(echo "$CLIENT_PRIVATE_KEY" | wg pubkey)

kill $ANIM_PID 2>/dev/null
wait $ANIM_PID 2>/dev/null
echo ""

# ‚úÖ –ò–∑–≤–ª–∏—á–∞–Ω–µ –Ω–∞ —Å—ä—Ä–≤—ä—Ä–Ω–∏—è –ø—É–±–ª–∏—á–µ–Ω –∫–ª—é—á –∏ –ø–æ—Ä—Ç
SERVER_PRIVATE_KEY=$(sudo grep '^PrivateKey' "$WG_CONF" | awk '{print $3}')
SERVER_PUBLIC_KEY=$(echo "$SERVER_PRIVATE_KEY" | wg pubkey)
WG_PORT=$(sudo grep '^ListenPort' "$WG_CONF" | awk '{print $3}')

# ‚úÖ –°—ä–∑–¥–∞–≤–∞–Ω–µ –Ω–∞ –∫–ª–∏–µ–Ω—Ç—Å–∫–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è
echo "üìÑ –°—ä–∑–¥–∞–≤–∞–Ω–µ –Ω–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –∑–∞ –∫–ª–∏–µ–Ω—Ç–∞: $CLIENT_NAME"

cat <<EOF | sudo tee "$CLIENT_CONF" > /dev/null
[Interface]
PrivateKey = $CLIENT_PRIVATE_KEY
Address = $CLIENT_IP/32
DNS = 1.1.1.1

[Peer]
PublicKey = $SERVER_PUBLIC_KEY
Endpoint = $SERVER_IP:$WG_PORT
AllowedIPs = $ALLOWED_IPS
PersistentKeepalive = 25
EOF

sudo chmod 600 "$CLIENT_CONF"

# ‚úÖ –î–æ–±–∞–≤—è–Ω–µ –Ω–∞ –∫–ª–∏–µ–Ω—Ç–∞ –≤ —Å—ä—Ä–≤—ä—Ä–Ω–∞—Ç–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è (—Å –º–∞—Ä–∫–µ—Ä–∏)
{
  echo "# $CLIENT_NAME START"
  echo "[Peer]"
  echo "PublicKey = $CLIENT_PUBLIC_KEY"
  echo "AllowedIPs = $CLIENT_IP/32"
  echo "# $CLIENT_NAME END"
} | sudo tee -a "$WG_CONF" > /dev/null

# ‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è—Ç–∞ —á—Ä–µ–∑ wg-quick strip (–≤–∞–ª–∏–¥–∏—Ä–∞ —Å–∏–Ω—Ç–∞–∫—Å–∏—Å–∞)
echo -n "üîç –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è—Ç–∞"
animate_dots 0.3 &
ANIM_PID=$!

if sudo wg-quick strip wg0 >/dev/null 2>&1; then
  kill $ANIM_PID 2>/dev/null
  wait $ANIM_PID 2>/dev/null
  echo ""
  echo -e "‚úÖ \e[32m–ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è—Ç–∞ –µ –≤–∞–ª–∏–¥–Ω–∞. –†–µ—Å—Ç–∞—Ä—Ç–∏—Ä–∞–Ω–µ –Ω–∞ WireGuard...\e[0m"
  sudo systemctl restart wg-quick@wg0 && echo -e "‚úÖ \e[32mWireGuard –µ —Ä–µ—Å—Ç–∞—Ä—Ç–∏—Ä–∞–Ω —É—Å–ø–µ—à–Ω–æ.\e[0m"
else
  kill $ANIM_PID 2>/dev/null
  wait $ANIM_PID 2>/dev/null
  echo ""
  echo -e "‚ùå \e[31m–ì—Ä–µ—à–∫–∞ –≤ –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è—Ç–∞! WireGuard –ù–ï –µ —Ä–µ—Å—Ç–∞—Ä—Ç–∏—Ä–∞–Ω.\e[0m"
  exit 1
fi

# ‚úÖ –ì–µ–Ω–µ—Ä–∏—Ä–∞–Ω–µ –Ω–∞ QR –∫–æ–¥
if command -v qrencode >/dev/null 2>&1; then
  echo "üì± –ì–µ–Ω–µ—Ä–∏—Ä–∞–Ω–µ –Ω–∞ QR –∫–æ–¥ –∑–∞ –º–æ–±–∏–ª–µ–Ω –∏–º–ø–æ—Ä—Ç..."
  qrencode -t ansiutf8 < "$CLIENT_CONF"

  # ‚úÖ –ó–∞–ø–∏—Å –∏ –∫–∞—Ç–æ PNG —Ñ–∞–π–ª
  sudo qrencode -o "$CLIENTS_DIR/$CLIENT_NAME.png" < "$CLIENT_CONF"
  echo "‚úÖ QR –∫–æ–¥—ä—Ç –µ –∑–∞–ø–∏—Å–∞–Ω –≤ $CLIENTS_DIR/$CLIENT_NAME.png"
else
  echo "‚ö†Ô∏è qrencode –Ω–µ –µ –Ω–∞–ª–∏—á–µ–Ω. –ü—Ä–æ–ø—É—Å–∫–∞–Ω–µ –Ω–∞ QR –∫–æ–¥."
fi

# ‚úÖ –§–∏–Ω–∞–ª–Ω–æ —Å—ä–æ–±—â–µ–Ω–∏–µ
echo ""
echo -e "üéâ \e[32m–ö–ª–∏–µ–Ω—Ç—ä—Ç '$CLIENT_NAME' –µ –¥–æ–±–∞–≤–µ–Ω —É—Å–ø–µ—à–Ω–æ!\e[0m"
echo "üìÇ –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è: $CLIENT_CONF"
echo "üåê IP –∞–¥—Ä–µ—Å: $CLIENT_IP"
echo "üì± QR –∫–æ–¥: $CLIENTS_DIR/$CLIENT_NAME.png"
echo ""
echo ""


