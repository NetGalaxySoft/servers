# =====================================================================

echo ""
echo -e "=============================================="
echo -e "   \e[1;32müéâ –ö–õ–ò–ï–ù–¢–™–¢ –ï –î–û–ë–ê–í–ï–ù –£–°–ü–ï–®–ù–û!\e[0m"
echo -e "=============================================="
echo ""
echo -e "üë§ \e[1;36m–ò–º–µ –Ω–∞ –∫–ª–∏–µ–Ω—Ç–∞:\e[0m       $CLIENT_NAME"
