#  —Å—ä—Ä–≤—ä—Ä. –¢–æ–π –¥–æ–±–∞–≤—è —É—Å–ª—É–≥–∏ –∑–∞ —Ö–æ—Å—Ç–∏–Ω–≥ –∏ —É–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –Ω–∞ –¥–æ–º–µ–π–Ω–∏.
